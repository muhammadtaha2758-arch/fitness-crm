            const search = this.tagSearch.toLowerCase().trim();
            if (!search) {
                // Show first 10 existing tags when input is empty
                this.filteredTagSuggestions = this.availableTags.slice(0, 10);
            } else {
                // Filter tags that match the search
                this.filteredTagSuggestions = this.availableTags
                    .filter(tag => tag.toLowerCase().includes(search))
                    .slice(0, 10);
            }
            this.showTagSuggestions = true;
            this.highlightedIndex = -1;
        },

        onTagInputFocus() {
            this.filterTagSuggestions();
            this.showTagSuggestions = true;
        },

        hideTagSuggestions() {
            // Delay hiding to allow click events on suggestions
            setTimeout(() => {
                this.showTagSuggestions = false;
                this.highlightedIndex = -1;
            }, 200);
        },

        navigateSuggestions(direction) {
            const totalSuggestions = this.filteredTagSuggestions.length + 
                (this.tagSearch.trim() && !this.filteredTagSuggestions.includes(this.tagSearch.trim()) && !this.member.tags.includes(this.tagSearch.trim()) ? 1 : 0);
            
            if (totalSuggestions === 0) return;
            
            if (direction === 'down') {
                this.highlightedIndex = (this.highlightedIndex + 1) % totalSuggestions;
            } else if (direction === 'up') {
                this.highlightedIndex = this.highlightedIndex <= 0 ? totalSuggestions - 1 : this.highlightedIndex - 1;
            }
            
            // Scroll highlighted item into view
            this.$nextTick(() => {
                const highlighted = document.querySelector('.tag-suggestion.highlighted');
                if (highlighted) {
                    highlighted.scrollIntoView({ block: 'nearest', behavior: 'smooth' });
                }
            });
        },

        handleTagEnter() {
            // If a suggestion is highlighted, add that tag
            if (this.highlightedIndex >= 0 && this.highlightedIndex < this.filteredTagSuggestions.length) {
                this.addTag(this.filteredTagSuggestions[this.highlightedIndex]);
            } else if (this.highlightedIndex === this.filteredTagSuggestions.length) {
                // If "Create new" option is highlighted
                this.addTag();
            } else {
                // Otherwise, add the typed text
                this.addTag();
            }
        },

        addTag(tagName = null) {
            const tagToAdd = tagName || this.tagSearch.trim();
            if (tagToAdd && !this.member.tags.includes(tagToAdd)) {
                this.member.tags.push(tagToAdd);
                this.tagSearch = "";
                this.showTagSuggestions = false;
                this.highlightedIndex = -1;
                
                // If it's a new tag not in availableTags, add it for future suggestions
                if (!this.availableTags.includes(tagToAdd)) {
                    this.availableTags.push(tagToAdd);
                    this.availableTags.sort();
                }
            }
        },

        removeTag(tag) {
            const index = this.member.tags.indexOf(tag);
            if (index > -1) {
                this.member.tags.splice(index, 1);
            }
        },

        // Email validation methods
        onEmailInput() {
            // Reset states when user starts typing
            this.emailExists = false;
            this.emailAvailable = false;
            
            // Clear field error if user is typing
            if (this.fieldErrors.email && !this.fieldErrors.email.includes('already registered')) {
                delete this.fieldErrors.email;
            }
            
            // Clear existing timeout
            if (this.emailCheckTimeout) {
                clearTimeout(this.emailCheckTimeout);
            }
            
            // Debounce email checking - wait 800ms after user stops typing
            this.emailCheckTimeout = setTimeout(() => {
                this.checkEmailAvailability();
            }, 800);
        },

        async checkEmailAvailability() {
            // Clear timeout if still pending
            if (this.emailCheckTimeout) {
                clearTimeout(this.emailCheckTimeout);
                this.emailCheckTimeout = null;
            }

            // Basic email format validation
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (!this.member.email || !emailRegex.test(this.member.email)) {
                this.emailExists = false;
                this.emailAvailable = false;
                this.emailChecking = false;
                return;
            }

            // Don't check if email is too short
            if (this.member.email.length < 5) {
                return;
            }

            this.emailChecking = true;
            this.emailExists = false;
            this.emailAvailable = false;

            try {
                const response = await axios.get('members/check-email', {
                    params: { email: this.member.email }
                });

                if (response.data && response.data.exists) {
                    this.emailExists = true;
                    this.emailAvailable = false;
                    this.fieldErrors.email = 'This email is already registered. Please use a different email address.';
                    
                    // Show toast notification
                    this.$root.$bvToast.toast('This email is already registered. Please use a different email address.', {
                        title: 'Email Already Exists',
                        variant: 'warning',
                        solid: true,
                        autoHideDelay: 5000,
                        toaster: 'b-toaster-top-right',
                        appendToast: true
                    });
                } else {
                    this.emailExists = false;
                    this.emailAvailable = true;
                    // Clear email error if it was about existing email
                    if (this.fieldErrors.email && this.fieldErrors.email.includes('already registered')) {
                        delete this.fieldErrors.email;
                    }
                }
            } catch (error) {
                // Handle email check errors gracefully
                if (error.response) {
                    const status = error.response.status;
                    if (status === 422) {
                        // Validation error - email format is invalid
                        if (error.response.data.errors && error.response.data.errors.email) {
                            const emailErrors = error.response.data.errors.email;
                            if (emailErrors.some(e => e.includes('valid') || e.includes('format'))) {
                                this.fieldErrors.email = "Please enter a valid email address";
                                this.emailExists = false;
                                this.emailAvailable = false;
                            } else if (emailErrors.some(e => e.includes('unique') || e.includes('taken') || e.includes('already'))) {
                                this.fieldErrors.email = "This email address is already registered. Please use a different email.";
                                this.emailExists = true;
                                this.emailAvailable = false;
                            }
                        }
                    } else if (status === 500) {
                        // Server error - don't block user, just mark as unknown
                        this.emailExists = false;
                        this.emailAvailable = false;
                        // Only show error in development
                        if (process.env.NODE_ENV === 'development') {
                            console.warn('Email check server error:', error.response.data?.message || 'Unknown error');
                        }
                    }
                } else {
                    // Network error - don't block user
                    this.emailExists = false;
                    this.emailAvailable = false;
                    // Only log in development
                    if (process.env.NODE_ENV === 'development') {
                        console.warn('Email check network error:', error.message);
                    }
                }
            } finally {
                this.emailChecking = false;
            }
        },
    },
    watch: {
        // Watch for changes in birthday components and update dob field
        'member.birthdayDay': {
            handler() {
                this.updateDobField();
            }
        },
        'member.birthdayMonth': {
            handler() {
                this.updateDobField();
            }
        },
        'member.birthdayYear': {
            handler() {
                this.updateDobField();
            }
        }
    },
    async mounted() {
        // Ensure placeholders are visible when component mounts
        this.ensurePlaceholdersVisible();
        // Generate available years for birthday dropdown
        this.generateAvailableYears();
        // Set current date for member since field
        this.setCurrentDate();
        // Fetch available tags for autocomplete
        await this.fetchAvailableTags();
    },
};
