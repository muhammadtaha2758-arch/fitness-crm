                const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                if (!emailRegex.test(this.member.email.trim())) {
                    this.fieldErrors.email = "Please enter a valid email address";
                    isValid = false;
                } else if (this.emailExists) {
                    this.fieldErrors.email = "This email is already registered";
